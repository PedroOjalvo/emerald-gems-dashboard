CREATE OR REPLACE VIEW "public"."resumen_cartera_actual" AS
 WITH "posiciones" AS (
         SELECT "posiciones_actuales"."user_id",
            "posiciones_actuales"."ticker",
            "posiciones_actuales"."nombre",
            "posiciones_actuales"."tipo_activo",
            "posiciones_actuales"."cantidad_actual",
            "posiciones_actuales"."precio_compra_avg",
            "posiciones_actuales"."precio_actual",
            "posiciones_actuales"."coste_total_estimado",
            "posiciones_actuales"."valor_actual",
            "posiciones_actuales"."pnl_no_realizado",
            "posiciones_actuales"."rentabilidad_no_realizada",
            "posiciones_actuales"."capital_comprado_bruto",
            "posiciones_actuales"."capital_vendido_bruto",
            "posiciones_actuales"."comisiones_totales",
            "posiciones_actuales"."n_compras",
            "posiciones_actuales"."n_ventas",
            "posiciones_actuales"."ultima_actualizacion"
           FROM "public"."posiciones_actuales"
        ), "liq" AS (
         SELECT "activos"."user_id",
            (COALESCE("activos"."cantidad_total", (0)::double precision))::numeric AS "liquidez"
           FROM "public"."activos"
          WHERE ("activos"."ticker" = 'LIQUIDEZ'::"text")
        ), "movs" AS (
         SELECT "movimientos"."user_id",
            (COALESCE("sum"(
                CASE
                    WHEN ("movimientos"."tipo_operacion" = 'Liquidez'::"text") THEN "movimientos"."importe_liquidez"
                    ELSE (0)::double precision
                END), (0)::double precision))::numeric AS "capital_neto_aportado"
           FROM "public"."movimientos"
          GROUP BY "movimientos"."user_id"
        ), "agg" AS (
         SELECT "p"."user_id",
            COALESCE("sum"("p"."coste_total_estimado"), (0)::numeric) AS "coste_total",
            COALESCE("sum"("p"."valor_actual"), (0)::numeric) AS "valor_invertido",
            COALESCE("sum"("p"."pnl_no_realizado"), (0)::numeric) AS "pnl_no_realizado",
            COALESCE("sum"("p"."comisiones_totales"), (0)::numeric) AS "comisiones_totales"
           FROM "posiciones" "p"
          GROUP BY "p"."user_id"
        )
 SELECT "a"."user_id",
    "a"."coste_total",
    "a"."valor_invertido",
    COALESCE("l"."liquidez", (0)::numeric) AS "liquidez",
    ("a"."valor_invertido" + COALESCE("l"."liquidez", (0)::numeric)) AS "valor_total",
    "a"."pnl_no_realizado",
    (("a"."valor_invertido" + COALESCE("l"."liquidez", (0)::numeric)) - COALESCE("m"."capital_neto_aportado", (0)::numeric)) AS "pnl_total_estimado",
        CASE
            WHEN (COALESCE("m"."capital_neto_aportado", (0)::numeric) > (0)::numeric) THEN ((("a"."valor_invertido" + COALESCE("l"."liquidez", (0)::numeric)) - COALESCE("m"."capital_neto_aportado", (0)::numeric)) / "m"."capital_neto_aportado")
            ELSE (0)::numeric
        END AS "rentabilidad_total_estimada",
    "a"."comisiones_totales",
    COALESCE("m"."capital_neto_aportado", (0)::numeric) AS "capital_neto_aportado"
   FROM (("agg" "a"
     LEFT JOIN "liq" "l" ON (("l"."user_id" = "a"."user_id")))
     LEFT JOIN "movs" "m" ON (("m"."user_id" = "a"."user_id")));


