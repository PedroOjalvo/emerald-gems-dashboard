    (COALESCE("a"."cantidad_total", (0)::double precision))::numeric AS "liquidez_en_activos",
    ((("l"."aportes_liquidez" - "l"."salidas_por_compras") + "l"."entradas_por_ventas") - (COALESCE("a"."cantidad_total", (0)::double precision))::numeric) AS "diferencia"
   FROM ("liquidez_movs" "l"
