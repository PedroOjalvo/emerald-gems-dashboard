            WHEN ("m"."tipo_operacion" = 'Venta'::"text") THEN (- "m"."cantidad")
            ELSE (0)::double precision
        END), (0)::double precision))::numeric AS "cantidad_calculada",
    (COALESCE("a"."cantidad_total", (0)::double precision))::numeric AS "cantidad_en_activos",
    ((COALESCE("sum"(
        CASE
            WHEN ("m"."tipo_operacion" = 'Compra'::"text") THEN "m"."cantidad"
            WHEN ("m"."tipo_operacion" = 'Venta'::"text") THEN (- "m"."cantidad")
            ELSE (0)::double precision
        END), (0)::double precision))::numeric - (COALESCE("a"."cantidad_total", (0)::double precision))::numeric) AS "diferencia"
   FROM ("public"."movimientos" "m"
     LEFT JOIN "public"."activos" "a" ON ((("a"."user_id" = "m"."user_id") AND ("a"."ticker" = "m"."ticker"))))
  WHERE ("m"."ticker" <> 'LIQUIDEZ'::"text")
  GROUP BY "m"."user_id", "m"."ticker", "a"."cantidad_total"
 HAVING ("abs"(((COALESCE("sum"(
        CASE
            WHEN ("m"."tipo_operacion" = 'Compra'::"text") THEN "m"."cantidad"
            WHEN ("m"."tipo_operacion" = 'Venta'::"text") THEN (- "m"."cantidad")
            ELSE (0)::double precision
        END), (0)::double precision))::numeric - (COALESCE("a"."cantidad_total", (0)::double precision))::numeric)) > 0.000001);


