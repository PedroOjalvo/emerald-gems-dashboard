CREATE OR REPLACE VIEW "public"."posiciones_actuales" AS
 WITH "movs" AS (
         SELECT "movimientos"."user_id",
            "movimientos"."ticker",
            ("sum"(
                CASE
                    WHEN ("movimientos"."tipo_operacion" = 'Compra'::"text") THEN "movimientos"."cantidad"
                    WHEN ("movimientos"."tipo_operacion" = 'Venta'::"text") THEN (- "movimientos"."cantidad")
                    ELSE (0)::double precision
                END))::numeric AS "cantidad_actual",
            ("sum"(
                CASE
                    WHEN ("movimientos"."tipo_operacion" = 'Compra'::"text") THEN ("movimientos"."cantidad" * "movimientos"."precio_unidad")
                    ELSE (0)::double precision
                END))::numeric AS "capital_comprado_bruto",
            ("sum"(
                CASE
                    WHEN ("movimientos"."tipo_operacion" = 'Venta'::"text") THEN ("movimientos"."cantidad" * "movimientos"."precio_unidad")
                    ELSE (0)::double precision
                END))::numeric AS "capital_vendido_bruto",
            ("sum"(
                CASE
                    WHEN ("movimientos"."tipo_operacion" = ANY (ARRAY['Compra'::"text", 'Venta'::"text"])) THEN "movimientos"."comision"
                    ELSE (0)::double precision
                END))::numeric AS "comisiones_totales",
            ("count"(*) FILTER (WHERE ("movimientos"."tipo_operacion" = 'Compra'::"text")))::integer AS "n_compras",
            ("count"(*) FILTER (WHERE ("movimientos"."tipo_operacion" = 'Venta'::"text")))::integer AS "n_ventas"
           FROM "public"."movimientos"
          WHERE ("movimientos"."ticker" <> 'LIQUIDEZ'::"text")
          GROUP BY "movimientos"."user_id", "movimientos"."ticker"
        ), "base" AS (
         SELECT COALESCE("a"."user_id", "m"."user_id") AS "user_id",
            COALESCE("a"."ticker", "m"."ticker") AS "ticker",
            COALESCE("a"."nombre", "m"."ticker") AS "nombre",
            "a"."tipo_activo",
            COALESCE("m"."cantidad_actual", (0)::numeric) AS "cantidad_actual",
            (COALESCE("a"."precio_compra_avg", (0)::double precision))::numeric AS "precio_compra_avg",
            (COALESCE("a"."precio_actual", (0)::double precision))::numeric AS "precio_actual",
            "a"."ultima_actualizacion",
            COALESCE("m"."capital_comprado_bruto", (0)::numeric) AS "capital_comprado_bruto",
            COALESCE("m"."capital_vendido_bruto", (0)::numeric) AS "capital_vendido_bruto",
            COALESCE("m"."comisiones_totales", (0)::numeric) AS "comisiones_totales",
            COALESCE("m"."n_compras", 0) AS "n_compras",
            COALESCE("m"."n_ventas", 0) AS "n_ventas"
           FROM ("movs" "m"
             FULL JOIN "public"."activos" "a" ON ((("a"."user_id" = "m"."user_id") AND ("a"."ticker" = "m"."ticker"))))
          WHERE (COALESCE("a"."ticker", "m"."ticker") <> 'LIQUIDEZ'::"text")
        )
 SELECT "user_id",
    "ticker",
    "nombre",
    "tipo_activo",
    "cantidad_actual",
    "precio_compra_avg",
    "precio_actual",
    ("cantidad_actual" * "precio_compra_avg") AS "coste_total_estimado",
    ("cantidad_actual" * "precio_actual") AS "valor_actual",
    (("cantidad_actual" * "precio_actual") - ("cantidad_actual" * "precio_compra_avg")) AS "pnl_no_realizado",
        CASE
            WHEN (("cantidad_actual" * "precio_compra_avg") > (0)::numeric) THEN ((("cantidad_actual" * "precio_actual") - ("cantidad_actual" * "precio_compra_avg")) / ("cantidad_actual" * "precio_compra_avg"))
            ELSE (0)::numeric
        END AS "rentabilidad_no_realizada",
    "capital_comprado_bruto",
    "capital_vendido_bruto",
    "comisiones_totales",
    "n_compras",
    "n_ventas",
    "ultima_actualizacion"
   FROM "base"
  WHERE ("abs"("cantidad_actual") > 0.000001);


