CREATE OR REPLACE FUNCTION "public"."generar_snapshot_cartera"("p_user_id" "uuid", "p_fecha" "date" DEFAULT CURRENT_DATE) RETURNS "uuid"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
declare
  v_snapshot_id uuid;
  v_resumen record;
begin
  select *
  into v_resumen
  from public.resumen_cartera_actual
  where user_id = p_user_id;

  if v_resumen is null then
    raise exception 'No existe resumen_cartera_actual para user_id=%', p_user_id;
  end if;

  insert into public.snapshots_cartera (
    id,
    fecha_snapshot,
    valor_total,
    liquidez,
    coste_total,
    pnl_total,
    rentabilidad_total,
    observaciones,
    user_id
  )
  values (
    gen_random_uuid(),
    p_fecha,
    v_resumen.valor_total,
    v_resumen.liquidez,
    v_resumen.coste_total,
    v_resumen.pnl_total_estimado,
    v_resumen.rentabilidad_total_estimada,
    'Snapshot generado desde resumen_cartera_actual',
    p_user_id
  )
  returning id into v_snapshot_id;

  insert into public.snapshot_activos (
    snapshot_id,
    ticker,
    nombre,
    tipo_activo,
    cantidad,
    precio_compra_avg,
    precio_actual,
    valor_posicion,
    peso_cartera,
    pnl,
    rentabilidad,
    user_id
  )
  select
    v_snapshot_id,
    p.ticker,
    p.nombre,
    p.tipo_activo,
    p.cantidad_actual,
    p.precio_compra_avg,
    p.precio_actual,
    p.valor_actual,
    case
      when r.valor_total > 0 then p.valor_actual / r.valor_total
      else 0
    end as peso_cartera,
    p.pnl_no_realizado,
    p.rentabilidad_no_realizada,
    p.user_id
  from public.posiciones_actuales p
  join public.resumen_cartera_actual r
    on r.user_id = p.user_id
  where p.user_id = p_user_id;

  return v_snapshot_id;
end;
$$;


